package com.java.operadores;

public class IgualdadeRelacionais {

	public static void main(String[] args) {
		int a = 2;
		int b = 3;
		int c = 1;
		boolean result;
		System.out.println(result = a > b);
		System.out.println(result = a < b);
		System.out.println(result = a != b);
		System.out.println(result = a >= c);
		System.out.println(result = a <= c);
		System.out.println(result = a == 2);
	}

}
